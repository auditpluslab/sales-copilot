import { chatCompletion } from "./client"
import type { Insight, DeepDiveQuestion, SuggestionCard } from "@/types"

export async function generateSuggestions(
  insight: Insight,
  transcriptText: string
): Promise<{
  questions: DeepDiveQuestion[]
  proposals: SuggestionCard[]
}> {
  const DEEP_THINKING_PROMPT = `あなたは最高の営業コンサルタントです。以下の手順で、**深く考え**ながら提案を作ってください。

絶対に飛ばさないで、各ステップを丁寧にこなしてください。

---

## ステップ1: 会話の完全理解（必須）

まず、以下の質問に答えてください。

1. クライアントの発言で、**具体的な数字**は何ですか？
   - 回答例: 「月20件」「40時間」「300万円」
   - 回答: [ここに書く]

2. クライアントの発言で、**具体的なツール名**は何ですか？
   - 回答例: 「Excel」「Chatwork」「Zoom」
   - 回答: [ここに書く]

3. クライアントの発言で、**具体的な役職・人物**は誰ですか？
   - 回答例: 「営業部長」「藤本さん」「経営企画部」
   - 回答: [ここに書く]

4. クライアントが**困っている具体的なシーン**は何ですか？
   - 回答例: 「週次MTGで進捗を報告するとき、Excelを開くのに5分かかる」
   - 回答: [ここに書く]

5. クライアントが**最も欲しい状態**は何ですか？
   - 回答例: 「週次MTGで5分で終わらせたい」
   - 回答: [ここに書く]

---

## ステップ2: 課題の定義（必須）

上記の回答に基づいて、課題を定義してください。

**形式**: 「誰が、いつ、どんな状況で、何に困っているのか」
- 回答例: 「営業部長が、毎週月曜日の週次MTGで、各営業担当者の進捗を確認するために、Excelファイルを開いて集計するのに5分かかっている」
- 回答: [ここに書く]

---

## ステップ3: 解決策の具体化（必須）

上記の課題に対して、以下の3つの条件を満たす解決策を考えてください。

### 条件1: 10分で導入できる
- 回答例: 「Googleスプレッドシートに共有ファイルを作る」
- 回答: [ここに書く]

### 条件2: 既存のツールを活用する
- 回答例: 「すでに使っているExcelのままで、ファイルをOneDriveに置く」
- 回答: [ここに書く]

### 条件3: 具体的な数字で効果を示せる
- 回答例: 「集計時間が5分から1分になる」
- 回答: [ここに書く]

---

## ステップ4: 提案の作成（必須）

上記のステップ1-3を踏まえて、提案を作ってください。

**形式**:
「先ほどの『[クライアントの言葉をそのまま引用]』というお話でしたが、
[具体的なアクション：10分で導入できる、既存ツールを活用する]をするところから始めませんか？

具体的には、[さらに具体的なアクション：誰が、何を、どうする]をします。
これにより、[具体的な効果：数字で示す]が期待できます。

最初の[期間：1週間/3日など]で試して、[何を見て判断するか]を確認しませんか？」

---

## ステップ5: セルフチェック（必須）

作った提案に対して、以下のチェックを行ってください。

1. クライアントの言葉をそのまま引用しているか？
   - YES/NO: [ ]

2. 具体的な数字が含まれているか？
   - YES/NO: [ ]

3. 具体的なツール名が含まれているか？
   - YES/NO: [ ]

4. 「テンプレート」「ダッシュボード」「可視化」「ワークショップ」
   「PoC」「システム導入」「ソリューション」「DX推進」「デジタル変革」
   などの抽象的な言葉を使っていないか？
   - YES/NO: [ ]

5. 10分で導入できるレベルの具体性があるか？
   - YES/NO: [ ]

6. 既存のツールや状況を活用しているか？
   - YES/NO: [ ]

---

もし、上記のチェックでNOが1つでもあったら、ステップ4に戻って修正してください。

---

## 最終出力

ステップ1-5を完了したら、最後に以下のJSON形式で出力してください。

{
  "questions": [
    {
      "question": "質問文（クライアントの言葉を引用し、数字や具体名を含める）",
      "intent": "なぜこの質問を聞くのか",
      "category": "constraint | value | timeline | budget | decision | competition",
      "priority": 1〜3,
      "evidence": "この質問の根拠となる具体的な発話をそのまま引用"
    }
  ],
  "proposals": [
    {
      "title": "具体的な提案タイトル（クライアントの言葉を含める）",
      "body": "提案の詳細説明（数字、具体名、クライアントの言葉を必ず含める）",
      "reason": "なぜ今この提案が自然か（クライアントの発言を引用して説明）",
      "expected_value": "期待される効果（数字で示す）",
      "next_step_phrase": "次の一言（「まずは〇〇から始めませんか？」形式）",
      "evidence": "根拠となる発話をそのまま引用",
      "confidence": "high | medium | low",
      "rank": 1〜2
    }
  ]
}

---

では、以下の会話について、ステップ1から丁寧に実行してください。

## 現在のインサイト
${JSON.stringify(insight, null, 2)}

## 直近の会話内容
${transcriptText}`;

  const result = await chatCompletion([
    {
      role: "user",
      content: DEEP_THINKING_PROMPT,
    },
  ])

  // LLMの出力からMarkdownコードブロックを取り除く
  let content = result.content || "{}"
  content = content.replace(/```json\n?/g, '').replace(/```\n?/g, '').trim()

  return JSON.parse(content)
}
